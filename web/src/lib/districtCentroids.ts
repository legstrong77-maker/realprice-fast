// 解決同名問題：臺中市的「大安區」內部 key 是 "大安區b"，但 DB 回傳的是 "大安區"
// 取座標時，依 county_code 找對應 dict，再用 district 去查
export function getCentroid(countyCode: string, district: string): [number, number] | null {
  const dict = DISTRICT_CENTROIDS[countyCode];
  if (!dict) return null;
  // 先試精確比對
  if (dict[district]) return dict[district];
  // 臺中大安區特殊處理
  if (countyCode === "b" && district === "大安區") return dict["大安區b"];
  return null;
}
